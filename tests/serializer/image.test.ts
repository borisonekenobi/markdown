import {describe, expect, test} from 'vitest';
import {tmpSerialize} from '../../src/index.js';

describe('Images', () => {
	test('simple', async () => {
		const input = '<p><img src="/assets/images/san-juan-mountains.jpg" alt="The San Juan Mountains are beautiful" title="San Juan Mountains"></p>';

		const output = await tmpSerialize(input);
		expect(output).
			toBe(
				'![The San Juan Mountains are beautiful](/assets/images/san-juan-mountains.jpg "San Juan Mountains")\n');
	});
});

